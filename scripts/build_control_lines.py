def main():
    print(f'=== Building control_line table ===')
    print(f'Total records: {len(CONTROL_LINES)}')

    conn = sqlite3.connect(DB_PATH)
    c = conn.cursor()

    c.execute('DROP TABLE IF EXISTS control_line')
    c.execute('''CREATE TABLE control_line (
        id INTEGER PRIMARY KEY AUTOINCREMENT,
        province TEXT NOT NULL,
        year INTEGER NOT NULL,
        batch TEXT NOT NULL,
        ke_lei TEXT NOT NULL,
        control_score INTEGER NOT NULL,
        UNIQUE(province, year, batch, ke_lei)
    )''')
    c.execute('CREATE INDEX idx_cl_prov_year ON control_line(province, year)')

    for prov, year, batch, ke_lei, score in CONTROL_LINES:
        c.execute('INSERT OR REPLACE INTO control_line (province, year, batch, ke_lei, control_score) VALUES (?,?,?,?,?)',
                  (prov, year, batch, ke_lei, score))

    conn.commit()

    # Stats
    total = c.execute('SELECT COUNT(*) FROM control_line').fetchone()[0]
    provs = c.execute('SELECT COUNT(DISTINCT province) FROM control_line').fetchone()[0]
    years = c.execute('SELECT DISTINCT year FROM control_line ORDER BY year').fetchall()

    print(f'\nInserted: {total} records')
    print(f'Provinces: {provs}')
    print(f'Years: {[y[0] for y in years]}')

    print('\nSample (吉林):')
    for row in c.execute('SELECT province, year, batch, ke_lei, control_score FROM control_line WHERE province="吉林" ORDER BY year DESC, batch'):
        print(f'  {row}')

    print('\nAll provinces:')
    for row in c.execute('SELECT province, COUNT(*) FROM control_line GROUP BY province ORDER BY province'):
        print(f'  {row[0]}: {row[1]} records')

    conn.close()


if __name__ == '__main__':
    main()
